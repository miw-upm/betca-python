# Arithmetic operators: +, -, *, /, //, %, **
var = 5
print('1- ', '5/2 = ', var / 2)
print('2- ', '5//2 = ', var // 2)
print('3- ', '5%2 = ', var % 2)
print('4- ', '5**2 = ', var ** 2)

# Assignment operators: =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=
var *= 3
print('5- ', 'var *=3 ', var)

# Logical operators: and, or, not, ==, !=, >, <, >=, <=, is is not
print('6- ', '17>15>12 = ', 17 > var > 12)

