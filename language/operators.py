# Operators
# Arithmetic operators: +, -, *, /, //, %, **
int_var = 5  # var is created
print('5/2 = ', int_var / 2)
print('5//2 = ', int_var // 2)
print('5%2 = ', int_var % 2)
print('5**2 = ', int_var ** 2)
# Assignment operators: =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=
int_var *= 3
print('5*3 = ', int_var)
# Logical operators: and, or, not, ==, !=, >, <, >=, <=, is is not
print('17>15>12 = ', 17 > int_var > 12)
